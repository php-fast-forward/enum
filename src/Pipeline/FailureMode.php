namespace FastForward\Enum\Pipeline;

use FastForward\Enum\DescribedEnumInterface;
use FastForward\Enum\LabeledEnumInterface;
use FastForward\Enum\Trait\Comparable;
use FastForward\Enum\Trait\HasLabel;
use FastForward\Enum\Trait\HasNameLookup;
use FastForward\Enum\Trait\HasNameMap;
use FastForward\Enum\Trait\HasNames;
use FastForward\Enum\Trait\HasOptions;
use FastForward\Enum\Trait\HasValueMap;
use FastForward\Enum\Trait\HasValues;

enum FailureMode: string implements DescribedEnumInterface, LabeledEnumInterface
{
    use Comparable;
    use HasLabel;
    use HasNameLookup;
    use HasNameMap;
    use HasNames;
    use HasOptions;
    use HasValueMap;
    use HasValues;

    case StopOnFailure = 'stop_on_failure';
    case ContinueOnFailure = 'continue_on_failure';

    public function description(): string
    {
        return match ($this) {
            self::StopOnFailure => 'Stops the pipeline immediately when a stage fails.',
            self::ContinueOnFailure => 'Continues processing remaining stages even after a failure.',
        };
    }

    public function stopsOnFailure(): bool
    {
        return $this->is(self::StopOnFailure);
    }
}
